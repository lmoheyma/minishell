#include "libft.h"

char	*ft_strjoin_free(char *s1, char *s2)
{	
	int		i;
	int		j;
	char	*res;

	if (!s1 || !s2)
		return (NULL);
	res = malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (!res)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		res[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		res[i] = s2[j++];
		i++;
	}
	free(s1);
	res[i] = '\0';
	return (res);
}
